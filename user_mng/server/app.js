// ==========================================
// 1. 환경 변수(.env) 설정 영역
// ==========================================
// 왜 필요한가? DB 비밀번호, 포트 번호 등 외부에 노출되면 안 되는 민감한 설정값들을
// 소스 코드에 직접 하드코딩하지 않고 별도의 파일로 관리하기 위함입니다.
// 기본적으로 프로젝트 루트 경로의 .env 파일을 찾지만,
// 현재 구조에서는 database 폴더 안에 dbConfig.env로 관리하고 있기 때문에 path 옵션으로 위치를 명시해주었습니다.
require("dotenv").config({ path: "./database/dbConfig.env" });

// ==========================================
// 2. 서버 초기화 및 기본 설정 영역
// ==========================================
// 웹 서버 구축을 위한 프레임워크인 Express 모듈을 불러오고, 이를 실행해 app 객체를 만듭니다.
const express = require("express");
const app = express();

// 서버가 클라이언트의 요청을 대기할 포트(Port) 번호입니다.
// 변수로 관리하는 이유: 나중에 배포 환경에 따라 포트를 변경하거나,
// 다른 프로그램과 포트 충돌이 났을 때 이 변수 값 하나만 수정하면 되기 때문입니다.
const port = 3000;

// ==========================================
// 3. 미들웨어(Middleware) 등록 영역
// ==========================================
// 미들웨어란? 클라이언트의 요청(Request)이 라우터에 도달하기 전에 거쳐 가는 중간 처리기입니다.
// 주로 클라이언트가 보낸 데이터를 서버가 읽기 편한 형태로 가공하는 역할을 합니다.

// [3-1] form 태그 전송 데이터 파싱
// 왜 필요한가? HTML <form> 태그에서 POST 방식으로 데이터를 보낼 때 사용되는
// 'application/x-www-form-urlencoded' 형식의 데이터를 파싱해 req.body에 객체 형태로 담아줍니다.
// extended: false는 Node.js에 기본 내장된 querystring 모듈을 사용하겠다는 의미입니다.
app.use(express.urlencoded({ extended: false }));

// [3-2] JSON 데이터 파싱 (매우 중요)
// 왜 필요한가? 프론트엔드(Vue.js 등)에서 Axios나 Fetch API를 사용해 데이터를 보낼 때
// 주로 JSON 형식을 사용합니다. 이 미들웨어가 없으면 서버에서 클라이언트가 보낸 JSON 데이터를
// req.body로 읽을 수 없고 undefined가 뜹니다.
app.use(express.json());

// ==========================================
// 4. 라우팅(Routing) 등록 영역
// ==========================================
// 사용자가 어떤 URL로 접속했을 때, 서버가 어떤 처리를 하고 어떤 결과를 줄지 길을 안내해줍니다.

// - 기본 라우팅 (서버 상태 확인용)
// http://localhost:3000/ 으로 접속했을 때 서버가 정상적으로 켜져 있는지 확인하기 위한 간단한 응답입니다.
app.get("/", (req, res) => {
  res.send("Welcome!!");
});

// - 기능별 라우터 모듈 등록
// 왜 필요한가? 모든 라우팅 로직을 app.js 한 파일에 다 적으면 코드가 너무 길어지고 관리가 힘듭니다.
// 따라서 회원 관리와 관련된 라우팅은 user_router.js로 분리하고, 여기서 가져와서 연결만 해주는 것입니다.
const userRouter = require("./routers/user_router.js");

// 클라이언트가 "/" 경로로 시작하는 요청을 보내면, userRouter 내부의 설정된 경로들을 탐색하게 합니다.
// (실무에서는 회원 관련 API임을 명확히 하기 위해 app.use("/users", userRouter); 처럼 URL을 분리하기도 합니다.)
app.use("/", userRouter);

// ==========================================
// 5. Server 실행 영역
// ==========================================
// 위에서 설정한 포트(3000번)를 열고, 클라이언트의 요청을 기다리기 시작합니다.
app.listen(port, () => {
  console.log("Server Start");
  // 개발할 때 콘솔창에서 바로 클릭해 브라우저를 띄울 수 있도록 링크 형태로 출력해두면 편리합니다.
  console.log(`http://localhost:${port}`);
});
